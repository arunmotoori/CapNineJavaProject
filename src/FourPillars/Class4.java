package FourPillars;

public class Class4 {

	public static void main(String[] args) {
		
		int a = 5, b = 3;
		
		//Arithmetic Operators - Basic ones
		
		System.out.println(a+b); // Addition operator - 8
		System.out.println(a-b); // Subtraction operator - 2
		System.out.println(a*b); // Multiplication operator - 15
		System.out.println(a/b); // Division Operator - 1
		System.out.println(a%b); // Modulus Operation - 2

	}

}
