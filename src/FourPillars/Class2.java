package FourPillars;

public class Class2 {

	public static void main(String[] args) {
		
		byte a = 5;  // 1 Byte - 8 bits - -128 to 127
		short b = 555; // 2 Byte - 16 bits 
		int c = 55555; // 4 Byte - 32 bits  (Regular)
		long d = 5555555555555L; // 8 Byte - 64 bits
		
		float e = 1.23F;  // 4 Bytes - 32 bits
		double f = 1.23;  // 8 Bytes - 64 bits
		double g = 1.23F;
		
		char s = '$'; 
		boolean t = true; // 1 bit
		boolean k = false;
		
		String str = "Arun Motoori";  // String is a pre-defined class in Java
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(s);
		System.out.println(t);
		System.out.println(k);
		System.out.println(str);
	}

}
