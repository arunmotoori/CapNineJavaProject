package FourPillars;

public class Class8 {

	public static void main(String[] args) {
		
		// Assignment Operators - Simple Assignment Operators and Compound Assignment Operators
		
		int a = 5;  // Normal Assignment operator 
		
		System.out.println(a);
		
		a = 10;  // Updation or re-initialization
		
		System.out.println(a);
		
		int m,n,o,p,q;
		
		m = n = o = p = q = 5;
		
		System.out.println(m);
		System.out.println(n);
		System.out.println(o);
		System.out.println(p);
		System.out.println(q);
		
		

	}

}
