package FourPillars;

public class Class9 {

	public static void main(String[] args) {
		
		int a = 5, b = 4;
		
		a+=b; // a = a + b
		
		System.out.println(a); // 9
		
		a-=b; // a = a - b
		System.out.println(a); // 5
		
		a*=b; // a = a * b 
		System.out.println(a); // 20
		
		a/=b; // a = a / b
		System.out.println(a); // 5
		
		a%=b; // a = a % b;
		System.out.println(a); // 1
	}

}
