package FourPillars;

public class Class3 {

	public static void main(String[] args) {
		
		System.out.println("Arun Motoori"); // String Literal - "Arun Motoori"
		System.out.println(9); // Integer Literal - 9
		System.out.println(9999L); // Long Literal - 9999L
		System.out.println('s'); // Character Literal - 's'
		System.out.println(true); // Boolean Literal - true or false
		System.out.println(3.14); // Double Literal - 3.14
		System.out.println(3.14D); // Double Literal - 3.14D
		System.out.println(3.14F); // Floating Point Literal - 3.14F
 
	}

}
