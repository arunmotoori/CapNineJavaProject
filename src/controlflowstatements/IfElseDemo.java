package controlflowstatements;

public class IfElseDemo {

	public static void main(String[] args) {
		
		int a = 3, b = 4;
		
		if(a>b) {
			
			System.out.println("a is greater than b");
			System.out.println("a is greater than b");
			System.out.println("a is greater than b");
			
		} else {
			
			System.out.println("a is less than b");
			System.out.println("a is less than b");
			System.out.println("a is less than b");
			
		}
		
		System.out.println("End of this program");

	}

}
